package Aula03;

public class A03 {
    static void main() {
        //Diagrama de classes

        /*
        Separá em 3 quando criamos uma classe

        Cima: Classe (nome da classe sempre começa
        com letra maiuscula.)
        ex: Caneta

        Meio: atribudos: coisas que a classe terá
        ex: uma caneta tem uma tampa, uma cor, tinta, etc.

        Baixo: o "metodo". coisas que a classe pode fazer
        ex: uma caneta PODE escrever.

        -atributo e metodo começa sempre com letra minuscula

        -metodos sempre terminam com "();"
        ex: escrever();
        */

        //Modificadores de Visibilidade

        /*serve para por nivel de privacidade nos atributos e na classe

        + = publica (public)
        qualquer um pode usar oq estiver public

        - = privado (private)
        priva somente para você oq estiver no private

        # = protegido (protected)
        protege só pra quem tem autorização para usar oq estiver no private

        */
    }
}
